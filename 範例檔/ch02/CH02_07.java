//程式:CH02_07.java, 資料型態的轉換
public class CH02_07 {
    public static void main(String[ ] args) {
        int i=10;			
        byte b=(byte) i;	
        byte b1=65;			
        char c=(char)b1;	
        System.out.println("i="+i);
        System.out.println("b="+b);
        System.out.println("b1="+b1);
        System.out.println("c="+c);
    }
}